// array.lastIndexOf

// description:
// method signiture: lastIndexOf(value, fromIndex)
// finds last index of a value in an array
// example: arr = [1, 2, 1, 2, 3]
// arr.lastIndexOf(2) => index 3
// we get index 3 as a result because 3 is the last index where 2 is found

// if a value is not present in an array indexOf returns -1 as a result
// example: arr = [1, 2, 1, 2, 3]
// arr.lastIndexOf(5) => result -1 since 5 does not exist in the array